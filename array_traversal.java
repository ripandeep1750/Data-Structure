// Traversal in an array

import java.util.*;
class GFG{
public static void main(String[] args)
{
	// Initialise array
	int arr[] = { 1, 2, 3, 4 };

	// size of array
	int N = arr.length;

	// Traverse the element of arr[]
	for (int i = 0; i < N; i++) {

		// Print the element
		System.out.print(arr[i] + " ");
	}
}
}
